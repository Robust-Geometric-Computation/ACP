void CalcIntersectTriangles(Poly1 poly_, std::vector<Triangle>& vecTriangles,
                            std::vector<Triangle>& vecColors) {
  float dx = 0.1;
  float dy = 0.1;
  float dz = 0.1;

  int numx = 2 / dx;
  int numy = 2 / dy;
  int numz = 2 / dz;

  for (int s = 0; s < numz; ++s) {
    for (int r = 0; r < numy; ++r) {
      for (int c = 0; c < numx; ++c) {
        float xyz0[3] = {-1 + c * dx, -1 + r * dy, -1 + s * dz};
        float xyz1[3] = {-1 + (c + 1) * dx, -1 + (r + 1) * dy,
                         -1 + (s + 1) * dz};
        Cube1 cube1;
        cube1.Set(xyz0, xyz1);
        cube1.isIntersectWithSurface(poly_, vecTriangles, vecColors);
      }
    }
  }

  return;
}

