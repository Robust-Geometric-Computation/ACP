    vertex v;
    v.x = poly1.GetGradientValueX(x, y, z);
    v.y = poly1.GetGradientValueY(x, y, z);
    v.z = poly1.GetGradientValueZ(x, y, z);

    if (isnan(v.x)) v.x = 0;
    if (isnan(v.y)) v.y = 0;
    if (isnan(v.z)) v.z = 0;

    // normalize
    float len = sqrt(v.x * v.x + v.y * v.y + v.z * v.z) + 1e-10;
    v.x /= len;
    v.y /= len;
    v.z /= len;
    len = sqrt(v.x * v.x + v.y * v.y + v.z * v.z) + 1e-10;
    v.x = 0.5 * v.x + 0.5;
    v.y = 0.5 * v.y + 0.5;
    v.z = 0.5 * v.z + 0.5;
